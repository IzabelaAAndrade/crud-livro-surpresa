    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        
        if (jComboBox1.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Selecione o tipo de dados que você deseja excluir.", "Erro!", JOptionPane.WARNING_MESSAGE);
        }
        if (jComboBox1.getSelectedIndex() == 1){
            if(tfClienteCpfExcluir.getText().equals("")){
                 JOptionPane.showMessageDialog(null, "Insira um CPF válido para excluir.", "Erro!", JOptionPane.WARNING_MESSAGE);
            }else{
                String cpf = tfClienteCpfExcluir.getText();
                
                for(int cont=0; cont<list_cliente.size(); cont++){
                    if(list_cliente.get(cont).getCpf().equals(cpf)){
                        dc.remover(list_cliente.get(cont));
                        list_cliente.remove(list_cliente.get(cont));
                    }
                }
                
            }
        if (jComboBox1.getSelectedIndex() == 2){
             if(tfLivroIdExcluir.getText().equals("")){
                 JOptionPane.showMessageDialog(null, "Insira um ID válido para excluir.", "Erro!", JOptionPane.WARNING_MESSAGE);
            }else{
                 int id = Integer.parseInt(tfLivroIdExcluir.getText());
                
                for(int cont=0; cont<list_livro.size(); cont++){
                    if(list_livro.get(cont).getId() == id){ 
                        dl.remover(list_livro.get(cont));
                        list_livro.remove(list_livro.get(cont));
                    }
                }
            }
        
        }
        tfClienteCpfExcluir.setText("");
        tfLivroIdExcluir.setText("");
        listar();
    }//GEN-LAST:event_btExcluirActionPerformed
    }
