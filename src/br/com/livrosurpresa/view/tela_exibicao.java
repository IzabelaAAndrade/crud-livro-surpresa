    private void btSalvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvar1ActionPerformed
        // TODO add your handling code here:
        int qtd_livros = list_livro.size();
        int sorteado = -1;
        Recebe recebeu = new Recebe();
        Random r = new Random();
        for(int i = 0; i<list_cliente.size(); i++){
            recebeu.setCpf_cliente(list_cliente.get(i).getCpf());
            sorteado = r.nextInt(qtd_livros);
            for(int j=0; j<qtd_livros; j++){
                if(j == sorteado){
                    recebeu.setId_livro(list_livro.get(j).getId());
                    j = qtd_livros;
                }
            }
            dr.salvar(recebeu);
        }
        listar();
    }//GEN-LAST:event_btSalvar1ActionPerformed

